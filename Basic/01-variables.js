// var, a día de hoy es la que menos se utiliza o no se utiliza. Puede llegar a generar errores y confusiones. 

var helloWorld = "¡Hola, JavaScript!"
console.log(helloWorld)

helloWorld = "¡Hola de nuevo, JavaScript!"
console.log(helloWorld)

// let, es una forma más moderna y segura, para variables que cambian su valor.

let helloWorld2 = "¡Hola, JavaScript 2!"
console.log(helloWorld2)

helloWorld2 = "¡Hola de nuevo, JavaScript 2!"
console.log(helloWorld2)

// const, es una forma ideal para variables que no van a cambiar su valor.

const helloWorld3 = "¡Hola, JavaScript 3!"
console.log(helloWorld3)

